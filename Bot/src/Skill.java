
public class Skill{
	
	private char key;
	private String type;
	private boolean learned;
	
	public Skill(char key, String type) {
		this.key = key;
		this.type = type;
	}
	
	public char getKey() {
		return key;
	}
	
	public String getType() {
		return type;
	}
	
	public boolean getLearned() {
		return getLearned();
	}
	
	public void learn() {
		learned = true;
	}
	
}
